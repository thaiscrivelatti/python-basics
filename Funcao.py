# Funcao
# Possui tres partes: nome, parametros e corpo
# A palavra def determinar o inicio da funcao
# Pode haver parametros ou nao
# Utiliza-se return ao final quando necessario

def mensagem1():
    print("Hello world!")


def mensagem2():
    return 'Hello world again!'


mensagem1()

texto = mensagem2()
print(texto)

